public class incDecrOperator {

        public static void main (String args [ ] )
        {
            int x = 8, y = 13;
            System.out.println ("x =" + x);
            System.out.println ("y =" +y);
            System.out.println ("++x =" + ++x);//IMP
            System.out.println ("++y =" + ++y);//IMP
            System.out.println ("y++ =" + y++);//IMP
            System.out.println ("x =" + x);//IMP
            System.out.println ("y =" + y);//IMP
        }


}
